CREATE TABLE Customer_type(
	Customer_code_type varchar(2) PRIMARY KEY,
	Code_name_type varchar(40)
);

CREATE TABLE Loan_type(
	Loan_Name_type varchar(40),
	Loan_ID_type varchar(2) PRIMARY KEY
);
CREATE TABLE Account_type (
	Account_ID_type varchar(2) PRIMARY KEY,
	Account_Name_type varchar(40)
);

CREATE TABLE Customer(
	Customer_ID int NOT NULL PRIMARY KEY,
	Customer_Name nvarchar(50) NOT NULL,
	Birth DATE,
	Email varchar(40),
	Phone_number varchar(12) NOT NULL,
	Address nvarchar(100),
	Customer_code_type varchar(2) NOT NULL,
	Password varchar(40) NOT NULL,

	CONSTRAINT FK_Custype_Customer FOREIGN KEY (Customer_code_type) REFERENCES Customer_type(Customer_code_type)
);

CREATE TABLE Transaction_Type(
	Transaction_Code_Type varchar(2) NOT NULL PRIMARY KEY,
	Transaction_Name_Type varchar(40) NOT NULL
);

CREATE TABLE Branch(
	Branch_ID varchar(14) NOT NULL PRIMARY KEY,
	Branch_address nvarchar(100),
	Branch_phone varchar(12) NOT NULL
);

CREATE TABLE Account(
	Account_number bigint NOT NULL Primary key,
	Customer_ID int NOT NULL,
	Account_ID_type varchar(2) NOT NULL,
	Balance decimal(10,2) NOT NULL,
	Branch_ID varchar(14) NOT NULL,

	CONSTRAINT FK_accounttype_Account FOREIGN KEY (Account_ID_type) REFERENCES Account_type(Account_ID_type),
	CONSTRAINT FK_Customer_Account FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID),
	CONSTRAINT FK_BRANCH_ACCOUNT FOREIGN KEY (Branch_ID) REFERENCES Branch(Branch_ID)
);

CREATE TABLE Loan(
	Loan_ID bigint NOT NULL PRIMARY KEY,
	Account_number bigint NOT NULL,
	Loan_ID_type varchar(2) NOT NULL,
	Loan_amount decimal(10,2) NOT NULL,
	CONSTRAINT FK_account_Loan FOREIGN KEY (Account_number) REFERENCES Account(Account_number),
	CONSTRAINT FK_Loantype_Loan FOREIGN KEY (Loan_ID_type) REFERENCES Loan_type(Loan_ID_type)
);

CREATE TABLE Transactions(
	Transaction_ID int NOT NULL PRIMARY KEY,
	Account_number bigint NOT NULL,
	Transaction_code_type varchar(2) NOT NULL,
	Date_trans date NOT NULL,
	Transaction_amount decimal(10,2) NOT NULL,

	CONSTRAINT FK_Account_Transaction FOREIGN KEY (Account_number) REFERENCES Account(Account_number),
	CONSTRAINT FK_TransactionType_Transaction FOREIGN KEY (Transaction_Code_Type) REFERENCES Transaction_type(Transaction_Code_Type)
);

